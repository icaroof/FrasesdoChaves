package br.com.barrildobrado.chavesdebolso.model.vo;

public class FraseVO {
    private int id;
    private int idIcone;
    private int idBackground;
    private int idMedia;
    private int idPersonagem;
    private int idStrFrase;
    private int idAssinatura;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdIcone() {
        return idIcone;
    }

    public void setIdIcone(int idIcone) {
        this.idIcone = idIcone;
    }

    public int getIdBackground() {
        return idBackground;
    }

    public void setIdBackground(int idBackground) {
        this.idBackground = idBackground;
    }

    public int getIdMedia() {
        return idMedia;
    }

    public void setIdMedia(int idMedia) {
        this.idMedia = idMedia;
    }

    public int getIdPersonagem() {
        return idPersonagem;
    }

    public void setIdPersonagem(int idPersonagem) {
        this.idPersonagem = idPersonagem;
    }

    public int getIdStrFrase() {
        return idStrFrase;
    }

    public void setIdStrFrase(int idStrFrase) {
        this.idStrFrase = idStrFrase;
    }

    public int getIdAssinatura() {
        return idAssinatura;
    }

    public void setIdAssinatura(int idAssinatura) {
        this.idAssinatura = idAssinatura;
    }

}
